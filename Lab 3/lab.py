def Sort(t):
    if not t:
        return t
    else:
        return Sort(Remove(t, Maximum(t))) + (Maximum(t),)

def Maximum(t):
    if len(t) == 1:
        return t[0]
    elif t[0] <= t[1]:
        return Maximum(Remove(t, t[0]))
    elif t[0] > t[1]:
        return Maximum(Remove(t, t[1]))
        
def Remove(t, e):
    return t[:t.index(e)] + t[t.index(e) + 1:] if t else t

# Tests and return values are below

print(Maximum((1,)))                      #  1                            2 pt.
